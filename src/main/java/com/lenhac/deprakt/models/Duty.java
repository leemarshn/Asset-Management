package com.lenhac.deprakt.models;
import jakarta.persistence.*;
import lombok.Data;

import java.time.LocalDate;
import java.util.List;

@Entity
@Table(name = "tasks")
@Data
public class Duty extends BaseEntity {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "task_id", updatable = false, nullable = false)
    private Long taskId;

    @Column(name = "name", nullable = false)
    private String name;

    @Column(name = "description", nullable = false)
    private String description;

    @Column(name = "priority", nullable = false)
    private String priority; // Use an appropriate type (e.g., enum) for priority levels

    @Column(name = "recurrence")
    private String recurrence; // You can use an enum or other appropriate type for recurrence settings

    @ManyToMany(mappedBy = "duties") // Many-to-many relationship with Position
    private List<Position> positions;

}
